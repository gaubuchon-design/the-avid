// ─── Auto-Reframe (CC-01) ────────────────────────────────────────────────

export type SubjectType = 'face' | 'salient_object' | 'action_center' | 'custom_region';

export interface DetectedSubject {
  type: SubjectType;
  confidence: number;
  boundingBox: { x: number; y: number; width: number; height: number };
  trackId?: string;
}

export interface AutoReframeConfig {
  sourceAspect: AspectRatio;
  targetAspect: AspectRatio;
  subjectLockStrength: number;  // 0-1, how tightly the crop follows the subject
  motionSmoothing: number;      // 0-1, smoothing factor for crop motion
  subjectPriority: SubjectType[];
  safeZoneMargin: number;       // 0-0.2, margin around detected subject
}

export interface ReframeKeyframe {
  time: number;
  cropRegion: { x: number; y: number; width: number; height: number };
  subjects: DetectedSubject[];
}

export interface ReframeResult {
  clipId: string;
  sourceAspect: AspectRatio;
  targetAspect: AspectRatio;
  keyframes: ReframeKeyframe[];
  subjectRetentionScore: number; // 0-1, percentage of frames with subject visible
  status: 'pending' | 'processing' | 'completed' | 'failed';
  error?: string;
}

