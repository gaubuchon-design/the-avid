// ─── Beat Sync (CC-06) ───────────────────────────────────────────────────

export type BeatSyncMode = 'auto_cut' | 'markers' | 'speed_ramp';

export interface BeatSyncConfig {
  mode: BeatSyncMode;
  beatThreshold: number;       // 0-1, minimum beat strength to sync to
  sourceClipIds: string[];
  audioTrackId?: string;       // which audio track to detect beats from
  everyNBeats: number;         // for auto_cut: cut every N beats (1, 2, 4)
  speedRampIntensity?: number; // 0-1, how much speed variation in ramp mode
  quantize?: boolean;          // snap cuts to nearest beat
  transitionType?: 'cut' | 'crossfade' | 'whip';
  transitionDuration?: number; // seconds
}

export interface BeatSyncResult {
  id: string;
  config: BeatSyncConfig;
  detectedBPM: number;
  beats: BeatMarker[];
  cuts: Array<{ time: number; clipId: string }>;
  markers: Array<{ time: number; label: string }>;
  speedKeyframes?: Array<{ time: number; speed: number }>;
  status: 'pending' | 'processing' | 'completed' | 'failed';
  error?: string;
}

